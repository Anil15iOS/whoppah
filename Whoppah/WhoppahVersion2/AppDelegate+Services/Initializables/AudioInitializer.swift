import Foundation
import AVFoundation

struct AudioInitializer: AppInitializable {
    func didFinishLaunchingApplication() {
        do {
            try AVAudioSession.sharedInstance().setCategory(.ambient, mode: .default, options: [])
            try AVAudioSession.sharedInstance().setActive(true)
        } catch {}
    }
}
